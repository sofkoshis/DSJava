public class Nested {
    public static void main(String[] args)  {

    for (int  dots = 1; dots<= 5; dots++) {
        //make the spaces before numbers
        
        for (int space= 5; space > dots; space--) {
            System.out.print(".");
        }
            //make the 'dots' print 'dots' times ex. 1 printed one time 2 printed two times
        for (int num = 1; num <= dots; num++) {
            System.out.printf("%d", dots);
        }
        }


    }
    }

